                    <p>Как только въезжаем в Сокольское, сразу поворачиваем направо в сторону поселка Летняя База. Через 6 км поворачиваем налево в деревню Кудрино. Едем прямо и пересекаем мостик с речкой. Проезжаем деревню, через 500 метров лес.</p>
